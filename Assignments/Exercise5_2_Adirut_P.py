S = int(input("กรุณาใส่ระยะทางที่เคลื่อนที่ (km) : "))
t = int(input("กรุณาใส่เวลาที่ใช้ในการเคลื่อนที่ (hr) : "))

v = int(S / t)
print("------------------------------------------")
print("ระยะทางที่เคลื่อนที่คือ", S, "km")
print("เวลาที่ใช้ในการเคลื่อนที่คือ", t, "hr")
print("------------------------------------------")
print("อัตราเร็วของการเคลื่อนที่คือ", v, "km/hr")